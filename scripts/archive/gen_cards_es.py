def translate_title(title):
    """Look up title in map, or return original."""
    if title in TITLE_MAP:
        return TITLE_MAP[title]
    # Try lowercase lookup
    for k, v in TITLE_MAP.items():
        if k.lower() == title.lower():
            return v
    return title  # Keep original if not found


def translate_text(text):
    """Translate a card text string EN→ES using phrase dictionary."""
    result = text
    
    # Replace month ranges: "January–March" → "Enero–Marzo"
    def replace_months(m):
        m1, dash, m2 = m.group(1), m.group(2), m.group(3)
        return f"{MONTHS.get(m1, m1)}{dash}{MONTHS.get(m2, m2)}"
    
    result = re.sub(
        r'\b(January|February|March|April|May|June|July|August|September|October|November|December)'
        r'([–\-–])'
        r'(January|February|March|April|May|June|July|August|September|October|November|December)\b',
        replace_months, result
    )
    
    # Replace standalone month names not part of ranges
    for en, es in MONTHS.items():
        result = re.sub(rf'\b{re.escape(en)}\b', es, result)
    
    # Replace longest phrases first, using word boundaries to avoid corrupting proper nouns
    sorted_phrases = sorted(PHRASES.keys(), key=len, reverse=True)
    for en_phrase in sorted_phrases:
        es_phrase = PHRASES[en_phrase]
        # Use word boundary for single words, exact match for multi-word phrases
        if ' ' in en_phrase or '-' in en_phrase:
            # Multi-word: simple replace (safe)
            result = result.replace(en_phrase, es_phrase)
            result = result.replace(en_phrase.capitalize(), es_phrase.capitalize())
        else:
            # Single word: use word boundary to avoid partial matches
            result = re.sub(r'\b' + re.escape(en_phrase) + r'\b', es_phrase, result)
    
    return result


