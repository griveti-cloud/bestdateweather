import csv

INPUT = 'data/cards_en.csv'
OUTPUT = 'data/cards_es.csv'

rows = list(csv.DictReader(open(INPUT, encoding='utf-8-sig')))
print(f"Processing {len(rows)} rows...")

untranslated_titles = set()

with open(OUTPUT, 'w', newline='', encoding='utf-8') as f:
    writer = csv.DictWriter(f, fieldnames=['slug', 'icon', 'title', 'text'])
    writer.writeheader()
    for row in rows:
        es_title = translate_title(row['title'])
        es_text = translate_text(row['text'])
        if es_title == row['title']:
            untranslated_titles.add(row['title'])
        writer.writerow({
            'slug': row['slug'],
            'icon': row['icon'],
            'title': es_title,
            'text': es_text
        })

print(f"\n✅ Written: {OUTPUT}")
print(f"Untranslated titles ({len(untranslated_titles)}):")
for t in sorted(untranslated_titles)[:50]:
    print(f"  - {repr(t)}")
if len(untranslated_titles) > 50:
    print(f"  ... and {len(untranslated_titles)-50} more")
